/**
 * Eval block compilation for Deno (spec §5).
 *
 * Generates data: URI TypeScript modules from transformed eval block
 * source code, dynamically imports them, and returns the default-exported
 * generator function. Replaces the Node.js node:vm approach.
 *
 * Effection APIs and EMA globals are imported in the generated module
 * via standard import statements — no VM sandbox globals needed.
 */

import { createContext as createEffectionContext, call } from "effection";
import type { Operation } from "effection";

// ---------------------------------------------------------------------------
// EvalContext — lightweight context for the eval system
// ---------------------------------------------------------------------------

/**
 * Eval context for document runs.
 *
 * In the Deno model, there is no VM context — eval blocks are compiled
 * into data: URI modules that import their dependencies. The EvalContext
 * exists as a marker that the eval system has been initialized.
 */
export interface EvalContext {
  /** Placeholder for future per-document eval configuration. */
  initialized: true;
}

/**
 * Effection context key for the eval context.
 */
export const EvalCtxKey = createEffectionContext<EvalContext>("evalContext");

/**
 * Create an eval context for a document run.
 *
 * In the Deno model, this is lightweight — no VM sandbox creation.
 * The eval context is set on the Effection scope so eval blocks
 * can verify the eval system is initialized.
 */
export function createEvalContext(
  _globals: Record<string, unknown> = {},
): EvalContext {
  return { initialized: true };
}

// ---------------------------------------------------------------------------
// Standard imports for generated eval modules
// ---------------------------------------------------------------------------

/**
 * Import statements prepended to every generated eval module.
 * These provide the APIs that were previously available as VM globals.
 */
const STANDARD_IMPORTS = [
  'import { sleep, spawn, call, resource, useScope, createChannel, each, suspend, createSignal } from "effection";',
  'import { when } from "@effectionx/converge";',
  'import { fetch } from "@effectionx/fetch";',
  'import { useContent, findFreePort, Sample } from "@executablemd/core/globals";',
].join("\n");

// ---------------------------------------------------------------------------
// Block compiler — data: URI module import (spec §5.2)
// ---------------------------------------------------------------------------

/**
 * Compile transformed source code into a generator function via data: URI.
 *
 * Generates a TypeScript module source string containing:
 * 1. Standard Effection/EMA imports
 * 2. User import statements extracted from the eval block
 * 3. The generator function as default export
 *
 * The module is imported via a data: URI — no temp files, no filesystem
 * writes. The data: URI resolves imports through Deno's import map.
 *
 * @param transformedBodyCode - The transformed block body
 * @param userImports - Import declarations extracted from the eval block source
 * @param envKeys - Names of existing env bindings to destructure
 * @returns Operation yielding a generator function
 */
export function* compileBlock(
  transformedBodyCode: string,
  userImports: string[],
): Operation<(env: Record<string, unknown>) => Generator<unknown, unknown, unknown>> {
  const userImportLines = userImports.length > 0
    ? userImports.join("\n") + "\n"
    : "";

  // Note: the preamble (const { x, y } = env;) is already in the
  // transformedBodyCode — generated by transformBlock(). We do NOT
  // add a second preamble here.
  const moduleSource = [
    STANDARD_IMPORTS,
    userImportLines,
    `export default function*(env) {`,
    transformedBodyCode,
    `}`,
  ].join("\n");

  const dataUri = `data:application/typescript,${encodeURIComponent(moduleSource)}`;
  const mod: { default: (env: Record<string, unknown>) => Generator<unknown, unknown, unknown> } =
    yield* call(() => import(dataUri));

  if (typeof mod.default !== "function") {
    throw new Error(
      `compileBlock: expected default export to be a generator function, got ${typeof mod.default}`,
    );
  }

  return mod.default;
}
